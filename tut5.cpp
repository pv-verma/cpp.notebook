#include <iostream>
using namespace std;

int main() {

    int num1 , num2 ;
    cout << "enter the value for num1 :\n"; /* '<<' is insertion operator */
    cin >>num1 ; /* '>>' is an extraction operator*/

    cout << " enter the value for num2 :\n ";
    cin >>num2 ;

    cout << "the sum is "<<num1+num2 ;

    return 0;
}